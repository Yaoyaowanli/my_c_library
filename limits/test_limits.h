#ifndef LIMITS_H_MY_C_LIB
#define LIMITS_H_MY_C_LIB
//limits.h 里定义了整型的大小取值范围的宏常量；
//浮点型的取值限制在float.h
//特定宽度的整型和其他类型的限制在stdint.h

//  CHAR_BIT 8 or greater		char所占比特位
//  SCHAR_MIN -127 or less		signed char 最小值
//  SCHAR_MAX 127 or greater		signed char 最大值
//  UCHAR_MAX 255 or greater		unsigned char 最大值
//  CHAR_MIN SCHAR_MIN or 0		char 最小值
//  CHAR_MAX SCHAR_MAX or UCHAR_MAX	char 最大值
//  MB_LEN_MX 1 or greater 		多字节字符最大字节数
//  SHRT_MIN -32767 or less		signed short 最小值
//  SHRT_MAX 32767 or greater		signed short 最大值
//  USHRT_MAX 65535 or greater 		unsigned short 最大值
//  INT_MIN -32767 or less 		signed int 最小值
//  INT_MAX 32767 or greater 		signed int 最大值
//  UINT_MAX 65535 or greater 		unsigned int 最大值
//  
//  	还有long、long long 等就不列举了

#endif
